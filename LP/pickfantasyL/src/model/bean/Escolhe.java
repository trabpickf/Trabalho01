    public void setMelhorTime(String melhorTime) {
        this.melhorTime = melhorTime;
    }

    public String getPiorTime() {
        return piorTime;
    }

    public void setPiorTime(String piorTime) {
        this.piorTime = piorTime;
    }
    
    
    
